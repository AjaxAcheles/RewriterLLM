"""
09_train_style — style-conditioning adapter (Phase B3, optional).

Pipeline position: M8 — Training, phase B3 (optional, does not block completion)
Depends on: models/ftpo_model/ (M7), conditioned training pairs (curated separately)
Produces:   models/style_lora/, reports/style_eval.json

Adds a LoRA adapter on top of the M7 model that bends the editing output toward a target
author's voice when given a 3-5 paragraph style reference in the user turn. Without a style
reference, the model behaves identically to the base M7 editor (the adapter is a pure
extension, not a replacement).

This module is optional: the base editor (M6+M7) is a complete, usable product without it.
M8 should only be started after M7's gate checks pass and the base editor is stable.

HOW STYLE CONDITIONING WORKS
The STYLE_USER_TEMPLATE in prompt_config.py wraps the style reference in <style_reference>
tags and prepends it to the standard editing prompt. The model sees this tag structure in
conditioned training examples and learns to adapt its output register, sentence rhythm, and
vocabulary toward the reference while still removing slop and preserving content.

TRAINING REGIME
  - Rank 16 (not 32): the style signal is weaker than the editing signal; smaller adapter.
  - LR 5e-5 (same as FTPO): conservative to avoid overwriting M6/M7 behaviour.
  - 2 epochs maximum: style generalisation needs variety, not repetition; more epochs risks
    overfitting to specific reference authors.
  - Train ONLY on conditioned pairs (style reference included in every example). Do not mix
    with unconditioned M6/M7 pairs. Mixing causes the adapter to partially suppress the base
    editing capability when no style reference is present.

GATE (must pass before declaring M8 done):
  - No regression on ner_preservation, sem_vs_target, slop_delta vs. ftpo_eval.json baseline.
  - Conditioned outputs measurably shift toward the reference style (measured by a style
    similarity metric — cosine similarity between output and reference embeddings is a
    reasonable proxy).

Usage:
    python scripts/09_train_style.py [--base models/ftpo_model]
                                     [--pairs data/style_pairs.jsonl]
                                     [--output models/style_lora]
    # then evaluate:
    python scripts/run_eval.py models/style_lora reports/style_eval.json

Key implementation notes:
  - Conditioned training pairs (data/style_pairs.jsonl) must be curated separately — not
    generated by the M4 pipeline. Each pair needs a matching style reference excerpt from the
    same author as the clean target.
  - The STYLE_USER_TEMPLATE and STYLE_SYSTEM_PROMPT from prompt_config.py handle the
    message construction. Call build_messages(sloppy, clean, style_reference=...) for each pair.
  - Use the same DataCollatorForCompletionOnlyLM + RESPONSE_TEMPLATE setup as M6 to ensure
    completion-only loss masking. The same silent failure (model copies input) applies here.
"""

from pathlib import Path


def main(base_model="models/ftpo_model", pairs_path="data/style_pairs.jsonl",
         output_dir="models/style_lora"):
    """Train the style-conditioning adapter on top of the M7 FTPO model."""
    import torch
    from pathlib import Path as P
    from unsloth import FastLanguageModel
    from datasets import load_dataset
    from trl import SFTTrainer, SFTConfig, DataCollatorForCompletionOnlyLM
    try:
        from scripts.prompt_config import RESPONSE_TEMPLATE
    except ImportError:
        from prompt_config import RESPONSE_TEMPLATE

    if not P(pairs_path).exists():
        raise SystemExit(
            f"Style pairs not found: {pairs_path}. "
            "Run: python scripts/build_style_pairs.py"
        )

    MAX_SEQ = 8192

    model, tokenizer = FastLanguageModel.from_pretrained(
        base_model, max_seq_length=MAX_SEQ, dtype=None, load_in_4bit=True
    )
    model = FastLanguageModel.get_peft_model(
        model, r=16, lora_alpha=16, lora_dropout=0.05, bias="none",
        target_modules=["q_proj", "k_proj", "v_proj", "o_proj",
                        "gate_proj", "up_proj", "down_proj"],
        use_gradient_checkpointing="unsloth", random_state=42,
    )

    ds = load_dataset("json", data_files={"train": pairs_path})
    response_ids = tokenizer.encode(RESPONSE_TEMPLATE, add_special_tokens=False)
    collator = DataCollatorForCompletionOnlyLM(response_ids, tokenizer=tokenizer)

    trainer = SFTTrainer(
        model=model, tokenizer=tokenizer,
        train_dataset=ds["train"], data_collator=collator,
        dataset_text_field="text", max_seq_length=MAX_SEQ,
        args=SFTConfig(
            per_device_train_batch_size=1,
            gradient_accumulation_steps=8,
            num_train_epochs=2,
            learning_rate=5e-5,
            warmup_steps=20,
            lr_scheduler_type="cosine",
            fp16=not torch.cuda.is_bf16_supported(),
            bf16=torch.cuda.is_bf16_supported(),
            optim="adamw_8bit",
            logging_steps=25,
            output_dir="checkpoints/style",
            report_to="none",
        ),
    )
    trainer.train()

    P(output_dir).mkdir(parents=True, exist_ok=True)
    model.save_pretrained(output_dir)
    tokenizer.save_pretrained(output_dir)
    print(f"Style pass complete → {output_dir}")
    print("Run: python scripts/run_eval.py models/style_lora reports/style_eval.json")


if __name__ == "__main__":
    main()
